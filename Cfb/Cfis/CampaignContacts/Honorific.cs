        Mr,
        /// <summary>
        /// Mrs. or Missus
        /// </summary>
        Mrs,
        /// <summary>
        /// Ms. or Miss
        /// </summary>
        Ms,
        /// <summary>
        /// Rev. or Reverend
        /// </summary>
        Rev,
        /// <summary>
        /// Sister
        /// </summary>
        Sister
    }
}
